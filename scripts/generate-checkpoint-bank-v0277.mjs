function summarize(cards, acts) {
  const questions = cards.flatMap((card) => card.questions)
  const choices = questions.flatMap((question) => question.choices)
  const distribution = cards.reduce((counts, card) => {
    counts[card.proposedQuestionCount] = (counts[card.proposedQuestionCount] ?? 0) + 1
    return counts
  }, {})
  return {
    stageCount: acts.length,
    learningCardCount: cards.length,
    totalQuestions: questions.length,
    totalChoices: choices.length,
    totalDistractors: choices.filter((choice) => !choice.isCorrect).length,
    questionCountDistribution: distribution,
    allQuestionsHaveFourChoices: questions.every((question) => question.choices.length === 4),
    allQuestionsHaveOneCorrect: questions.every((question) => question.choices.filter((choice) => choice.isCorrect).length === 1),
    allWrongChoicesHaveFeedback: choices.filter((choice) => !choice.isCorrect).every((choice) => Boolean(choice.feedback)),
    allChoicesHaveStableIds: choices.every((choice) => Boolean(choice.choiceId)),
    allQuestionsHaveStableIds: questions.every((question) => Boolean(question.questionId))
  }
}

