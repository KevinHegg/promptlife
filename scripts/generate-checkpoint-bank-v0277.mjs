function makeQuestion(lesson, context, number, type) {
  const questionId = `draft-${slug(lesson.id)}-q${number}`
  const base = {
    questionId,
    type,
    misconceptionTags: [],
    glossaryTerms: context.directTerms.map((term) => term.id)
  }
  const direct = context.directTerms[0]
  const second = context.directTerms[1]
  const title = lesson.title
  const definition = sentence(lesson.definition, `${title} names a key part of the model story.`)
  const mechanism = sentence(lesson.durableVsTemporary || lesson.promptVsResponseNote || lesson.whereItHappens || lesson.coreExplanation || lesson.definition)
  const relationship = sentence(lesson.howItConnects || lesson.whyItMatters || lesson.definition)
  const why = sentence(lesson.whyItMatters || lesson.misconception || lesson.definition)
  const misconception = lesson.misconception || `Confusing ${title} with a nearby model concept.`

  const variants = {
    definition: {
      question: number === 1 ? explicitQuestion(lesson) : `Which statement best defines ${title} for Prompt Life learners?`,
      correct: lesson.quiz?.answer || definition,
      success: lesson.quiz?.explain || `Good distinction. ${definition}`,
      useLiveChoices: number === 1 && lesson.quiz?.choices?.length === 4
    },
    mechanism: {
      question: `Which mechanism or distinction does ${title} most directly teach?`,
      correct: mechanism || definition,
      success: `Good distinction. ${mechanism || definition}`
    },
    'boundary distinction': {
      question: `Which common confusion does ${title} help learners avoid?`,
      correct: `It corrects the misconception: ${misconception}`,
      success: `Good distinction. ${title} is clearer when that misconception is separated from the mechanism.`
    },
    'concept relationship': {
      question: `How does ${title} connect to the prompt's Journey?`,
      correct: relationship || `It connects ${direct?.term ?? title} to ${second?.term ?? 'the next model step'}.`,
      success: `Good connection. ${relationship || why}`
    },
    'applied scenario': {
      question: `A learner uses ${title} to explain an AI answer. What should they say?`,
      correct: why || definition,
      success: `Good model-literacy move. ${why || definition}`
    }
  }
  const variant = variants[type] ?? variants.definition

  let choices
  if (variant.useLiveChoices) {
    choices = lesson.quiz.choices.map((choiceText, index) => {
      const isCorrect = choiceText === lesson.quiz.answer
      const term = inferRepresentedTermForChoice(choiceText, context) ?? context.distractorPool[index] ?? null
      return {
        choiceId: `choice-${slug(questionId)}-${isCorrect ? 'correct' : slug(choiceText)}`,
        text: choiceText,
        isCorrect,
        feedback: isCorrect
          ? (lesson.quiz.explain || `Good distinction. ${definition}`)
          : (lesson.quiz.feedback?.[choiceText] || `This choice is tempting, but ${title} asks for ${definition}`),
        representedMisconception: isCorrect ? null : choiceText,
        representedGlossaryTerm: isCorrect ? null : term?.id ?? null,
        distractorSource: isCorrect ? null : term ? context.sourceFor(term) : 'authored live checkpoint distractor',
        distractorRationale: isCorrect ? null : `Existing live checkpoint distractor retained for review because it captures a plausible misunderstanding.`
      }
    })
  } else {
    const correctChoice = {
      choiceId: `choice-${slug(questionId)}-correct`,
      text: variant.correct,
      isCorrect: true,
      feedback: variant.success,
      representedMisconception: null,
      representedGlossaryTerm: null,
      distractorSource: null,
      distractorRationale: null
    }
    const pool = context.distractorPool.filter((term) => !context.directTerms.some((directTerm) => directTerm.id === term.id))
    const wrong = pool.slice(number, number + 3).map((term, index) => choiceFromTerm(term, lesson, { questionId }, context, index + 1))
    const fallback = fallbackWrongChoices(lesson, questionId)
    choices = [correctChoice, ...wrong, ...fallback].slice(0, 4)
  }

  if (choices.length < 4) {
    choices = [...choices, ...fallbackWrongChoices(lesson, questionId)].slice(0, 4)
  }

  const correctChoiceId = choices.find((choice) => choice.isCorrect)?.choiceId
  return {
    ...base,
    question: variant.question,
    correctChoiceId,
    choices,
    relatedGlossaryTerms: context.directTerms.map((term) => term.id),
    relatedStage: context.stage.title,
    nearbyStageSource: context.nearbyStageTerms.slice(0, 8).map((term) => term.id)
  }
}

function makeCardEntry(lesson, lessons, acts, glossary, byKey, meta, learningMap) {
  const context = contextForLesson(lesson, lessons, acts, glossary, byKey, meta, learningMap)
  const count = QUESTION_COUNT_BY_ID[lesson.id] ?? 3
  const questionTypes = QUESTION_TYPES.slice(0, count)
  const stageLessons = lessons.filter((item) => item.act === lesson.act)
  const currentLive = lesson.quiz ? {
    questionCount: 1,
    questions: [{
      question: lesson.quiz.question,
      choices: lesson.quiz.choices,
      correctAnswer: lesson.quiz.answer,
      feedback: lesson.quiz.feedback
    }]
  } : { questionCount: 0, questions: [] }

  return {
    stageNumber: context.stage.number,
    stageTitle: context.stage.title,
    learningCardNumberWithinStage: stageLessons.findIndex((item) => item.id === lesson.id) + 1,
    learningCardId: lesson.id,
    learningCardTitle: lesson.title,
    learningObjectives: {
      primary: `Explain ${lesson.title} as part of the day in the life of a prompt.`,
      secondary: [
        lesson.definition,
        lesson.whereItHappens,
        lesson.durableVsTemporary,
        lesson.promptVsResponseNote,
        lesson.howItConnects
      ].filter(Boolean).map((item) => sentence(item)),
      keyDistinctions: [
        lesson.durableVsTemporary,
        lesson.promptVsResponseNote,
        lesson.misconception
      ].filter(Boolean).map((item) => sentence(item)),
      misconceptionsToCorrect: lesson.misconception ? [lesson.misconception] : [],
      relatedGlossaryTerms: context.directTerms.map((term) => term.id),
      relatedNearbyGlossaryTerms: context.pathNearTerms.slice(0, 12).map((term) => term.id),
      learningPathNearTerms: context.pathNearTerms.slice(0, 12).map((term) => term.id),
      whyItMatters: lesson.whyItMatters || `This card helps learners make ${lesson.title} less mysterious.`
    },
    glossaryContext: {
      directTerms: context.directTerms.map((term) => term.id),
      sameStageTerms: context.sameStageTerms.slice(0, 20).map((term) => term.id),
      nearbyStageTerms: context.nearbyStageTerms.slice(0, 20).map((term) => term.id),
      confusableTerms: context.confusableTerms.slice(0, 20).map((term) => term.id)
    },
    currentLiveCheckpoint: currentLive,
    proposedQuestionCount: count,
    questionCountRationale: questionCountRationale(lesson, count),
    questions: questionTypes.map((type, index) => makeQuestion(lesson, context, index + 1, type))
  }
}

function makeGlossaryPathOrder(lessons, acts, glossary, byKey, meta, learningGroups) {
  const lessonById = new Map(lessons.map((lesson) => [lesson.id, lesson]))
  const actById = new Map(acts.map((act) => [act.id, act]))
  const groupEntries = new Map()
  learningGroups.forEach((group, groupIndex) => {
    group.termIds.forEach((termId, termIndex) => {
      groupEntries.set(termId.toLowerCase(), { group, groupIndex, termIndex })
    })
  })

  return glossary.map((term) => {
    const entry = groupEntries.get(term.id.toLowerCase())
    const lesson = entry?.group.firstLessonId ? lessonById.get(entry.group.firstLessonId) : null
    const stage = lesson ? actById.get(lesson.act) : null
    const nearbyLessons = lesson
      ? lessons.filter((item) => Math.abs((actById.get(item.act)?.number ?? 0) - (stage?.number ?? 0)) <= 1)
      : lessons.slice(0, 3)
    const nearbyTerms = uniqueTerms(nearbyLessons.flatMap((item) => item.terms).map((id) => findTerm(byKey, id)).filter(Boolean))
      .filter((item) => item.id !== term.id)
      .slice(0, 14)
    const confusable = meta[term.id]?.confusableWith ?? []
    return {
      stageNumber: stage?.number ?? null,
      stageTitle: stage?.title ?? 'Related / Foundational',
      learningCardId: lesson?.id ?? null,
      learningCardTitle: lesson?.title ?? null,
      mappingConfidence: lesson ? 'explicit learning-path hint' : 'inferred or unmapped',
      termId: term.id,
      glossaryTerm: term.term,
      shortDefinition: term.definition || meta[term.id]?.shortExplanation || '',
      relatedTerms: uniqueTerms((term.related ?? []).map((id) => findTerm(byKey, id)).filter(Boolean)).map((item) => item.id),
      confusableTerms: confusable,
      nearbyTerms: nearbyTerms.map((item) => item.id),
      distractorUseNotes: confusable.length
        ? `${term.term} is useful as a distractor when learners might confuse it with ${confusable.slice(0, 3).join(', ')}.`
        : `${term.term} can be used as a same-stage or nearby-stage distractor when the question asks for a more specific mechanism.`
    }
  }).sort((a, b) =>
    (a.stageNumber ?? 99) - (b.stageNumber ?? 99) ||
    String(a.learningCardTitle ?? '').localeCompare(String(b.learningCardTitle ?? '')) ||
    a.glossaryTerm.localeCompare(b.glossaryTerm)
  )
}

function mdEscape(value) {
  return cleanText(value).replace(/\|/g, '\\|')
}

function writeMarkdown(bank, glossaryOrder) {
  const lines = [
    `# Prompt Life Checkpoint Question Bank ${bank.version}`,
    '',
    `Status: ${bank.status}`,
    `Generated: ${bank.generatedAt}`,
    '',
    'This is a review draft only. It is not live in the Journey renderer.',
    '',
    '## Distractor Strategy',
    '',
    bank.strategy.summary,
    '',
    'Good distractors are misconception-first and concept-near. They are drawn from same-card terms, same-stage terms, nearby-stage terms, explicitly confusable glossary metadata, and learning-path-near terms.',
    '',
    '## Executive Summary',
    '',
    `- Learning cards: ${bank.cards.length}`,
    `- Proposed questions: ${bank.summary.totalQuestions}`,
    `- Answer choices: ${bank.summary.totalChoices}`,
    `- Wrong-answer distractors: ${bank.summary.totalDistractors}`,
    `- Distribution: ${JSON.stringify(bank.summary.questionCountDistribution)}`,
    '',
    '## Card Review Entries',
    ''
  ]

  bank.cards.forEach((card) => {
    lines.push(`### ${card.stageNumber}. ${card.stageTitle}: ${card.learningCardTitle}`)
    lines.push('')
    lines.push(`- ID: \`${card.learningCardId}\``)
    lines.push(`- Proposed question count: ${card.proposedQuestionCount}`)
    lines.push(`- Rationale: ${card.questionCountRationale}`)
    lines.push(`- Primary objective: ${card.learningObjectives.primary}`)
    lines.push(`- Current live checkpoint: ${card.currentLiveCheckpoint.questions[0]?.question ?? 'none'}`)
    lines.push(`- Direct glossary terms: ${card.glossaryContext.directTerms.join(', ') || 'none'}`)
    lines.push('')
    card.questions.forEach((question, index) => {
      lines.push(`#### Q${index + 1}. ${question.question}`)
      lines.push('')
      lines.push(`- Question ID: \`${question.questionId}\``)
      lines.push(`- Type: ${question.type}`)
      lines.push(`- Correct choice ID: \`${question.correctChoiceId}\``)
      lines.push('')
      question.choices.forEach((choice) => {
        lines.push(`- ${choice.isCorrect ? 'Correct' : 'Distractor'} \`${choice.choiceId}\`: ${choice.text}`)
        lines.push(`  Feedback: ${choice.feedback}`)
        if (!choice.isCorrect) {
          lines.push(`  Misconception: ${choice.representedMisconception || 'none recorded'}`)
          lines.push(`  Glossary term: ${choice.representedGlossaryTerm || 'none'}`)
          lines.push(`  Source: ${choice.distractorSource || 'none'}`)
        }
      })
      lines.push('')
    })
    lines.push('Review checklist: learning objectives accurate; topic named in question; one best answer; distractors plausible/fair; feedback explains misconception; no redundancy; count fits density; reading level right.')
    lines.push('')
  })

  const glossaryLines = [
    '# Glossary Learning Path Order v0.27.7',
    '',
    'Mapping confidence is explicit when it comes from the app learning-path hints; otherwise it is marked inferred or unmapped.',
    '',
    '| Stage | Learning card | Term | Definition | Related/confusable | Nearby terms | Distractor-use notes |',
    '|---|---|---|---|---|---|---|'
  ]
  glossaryOrder.forEach((entry) => {
    glossaryLines.push(`| ${entry.stageNumber ?? 'n/a'} ${mdEscape(entry.stageTitle)} | ${mdEscape(entry.learningCardTitle ?? 'n/a')} | ${mdEscape(entry.glossaryTerm)} | ${mdEscape(entry.shortDefinition)} | ${mdEscape([...entry.relatedTerms, ...entry.confusableTerms].join(', '))} | ${mdEscape(entry.nearbyTerms.join(', '))} | ${mdEscape(entry.distractorUseNotes)} |`)
  })

  fs.writeFileSync(path.join(CHECKPOINT_DIR, 'checkpoint-question-bank-v0-27-7-draft.md'), lines.join('\n'))
  fs.writeFileSync(path.join(CHECKPOINT_DIR, 'glossary-learning-path-order-v0-27-7.md'), glossaryLines.join('\n'))
}

