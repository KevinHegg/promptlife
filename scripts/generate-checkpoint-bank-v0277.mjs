function readSource(relativePath) {
  const filePath = path.join(root, relativePath)
  return ts.createSourceFile(filePath, fs.readFileSync(filePath, 'utf8'), ts.ScriptTarget.Latest, true, ts.ScriptKind.TS)
}

function propName(prop) {
  if (!ts.isPropertyAssignment(prop)) return null
  const name = prop.name
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name)) return name.text
  return null
}

function getProp(objectNode, name) {
  if (!objectNode || !ts.isObjectLiteralExpression(objectNode)) return null
  return objectNode.properties.find((prop) => propName(prop) === name)?.initializer ?? null
}

function cleanText(value = '') {
  return String(value)
    .replace(/\s+/g, ' ')
    .replace(/[“”]/g, '"')
    .replace(/[‘’]/g, "'")
    .trim()
}

function getString(node) {
  if (!node) return ''
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) return cleanText(node.text)
  if (ts.isTemplateExpression(node)) {
    return cleanText(node.getText().replace(/^`|`$/g, '').replace(/\$\{[^}]+\}/g, '...'))
  }
  return ''
}

function getNumber(node) {
  if (!node) return null
  if (ts.isNumericLiteral(node)) return Number(node.text)
  return null
}

function getStringArray(node) {
  if (!node || !ts.isArrayLiteralExpression(node)) return []
  return node.elements.map(getString).filter(Boolean)
}

function getStringMap(node) {
  if (!node || !ts.isObjectLiteralExpression(node)) return {}
  return Object.fromEntries(node.properties.flatMap((prop) => {
    const name = propName(prop)
    const value = ts.isPropertyAssignment(prop) ? getString(prop.initializer) : ''
    return name && value ? [[name, value]] : []
  }))
}

function getVariable(sourceFile, name) {
  let found = null
  sourceFile.forEachChild((node) => {
    if (!ts.isVariableStatement(node)) return
    node.declarationList.declarations.forEach((declaration) => {
      if (ts.isIdentifier(declaration.name) && declaration.name.text === name) {
        found = declaration.initializer
      }
    })
  })
  return found
}

function slug(value) {
  return cleanText(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 56) || 'choice'
}

function sentence(value, fallback = '') {
  const text = cleanText(value || fallback)
  if (!text) return ''
  const match = text.match(/^(.{24,220}?[.!?])(\s|$)/)
  return cleanText(match ? match[1] : text.slice(0, 220))
}

