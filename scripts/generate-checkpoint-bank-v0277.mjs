function termLookup(glossary) {
  const byKey = new Map()
  glossary.forEach((term) => {
    ;[term.id, term.term, ...term.aliases].filter(Boolean).forEach((key) => byKey.set(key.toLowerCase(), term))
  })
  return byKey
}

function findTerm(byKey, value) {
  if (!value) return null
  return byKey.get(String(value).toLowerCase()) ?? null
}

function explicitQuestion(lesson) {
  const current = lesson.quiz?.question || `Which statement best explains ${lesson.title}?`
  const title = lesson.title.replace(/\?+$/, '')
  const rewrite = VAGUE_QUESTION_REWRITES.find(({ pattern }) => pattern.test(current))
  if (rewrite) return rewrite.make(lesson)
  if (current.toLowerCase().includes(lesson.title.toLowerCase())) return current
  if (/^(what|why|how|which|when|where)\b/i.test(current)) {
    return `${current.replace(/\?$/, '')} in ${title}?`
  }
  return `In ${title}, ${current.charAt(0).toLowerCase()}${current.slice(1)}`
}

function questionCountRationale(lesson, count) {
  if (count === 2) return 'Light or narrow concept card; two checks should confirm the core distinction without overloading the learner.'
  if (count === 3) return 'Standard concept card; three checks cover definition, mechanism, and a nearby misconception.'
  if (count === 4) return 'Dense mechanism or high-confusion card; four checks cover definition, process, boundary, and relationship.'
  return 'Synthesis or badge-critical card; five checks cover multiple mechanisms and responsible-use distinctions.'
}

function stageForLesson(lesson, acts) {
  return acts.find((act) => act.id === lesson.act) ?? {
    id: lesson.act,
    number: 0,
    title: lesson.actLabel || lesson.section,
    shortTitle: lesson.actLabel || lesson.section
  }
}

function firstLessonMap(groups, lessons) {
  const lessonById = new Map(lessons.map((lesson) => [lesson.id, lesson]))
  const map = new Map()
  groups.forEach((group, groupIndex) => {
    group.termIds.forEach((termId, termIndex) => {
      const lesson = group.firstLessonId ? lessonById.get(group.firstLessonId) : null
      map.set(termId.toLowerCase(), {
        termId,
        firstLessonId: group.firstLessonId,
        learningOrder: groupIndex * 100 + termIndex,
        lesson
      })
    })
  })
  return map
}

function contextForLesson(lesson, lessons, acts, glossary, byKey, meta, learningMap) {
  const stage = stageForLesson(lesson, acts)
  const directTerms = lesson.terms.map((id) => findTerm(byKey, id)).filter(Boolean)
  const sameStageLessons = lessons.filter((item) => item.act === lesson.act)
  const stageNumbers = new Map(acts.map((act) => [act.id, act.number]))
  const nearbyActIds = acts
    .filter((act) => Math.abs((act.number || 0) - (stage.number || 0)) <= 1)
    .map((act) => act.id)
  const sameStageTerms = sameStageLessons.flatMap((item) => item.terms).map((id) => findTerm(byKey, id)).filter(Boolean)
  const nearbyStageTerms = lessons
    .filter((item) => nearbyActIds.includes(item.act) && item.act !== lesson.act)
    .flatMap((item) => item.terms)
    .map((id) => findTerm(byKey, id))
    .filter(Boolean)
  const confusableTerms = directTerms.flatMap((term) => [
    ...(meta[term.id]?.confusableWith ?? []),
    ...(meta[term.id]?.relationships ?? []),
    ...(term.related ?? [])
  ]).map((id) => findTerm(byKey, id)).filter(Boolean)
  const directOrders = directTerms.map((term) => learningMap.get(term.id.toLowerCase())?.learningOrder).filter((value) => value !== undefined)
  const anchor = directOrders.length ? Math.round(directOrders.reduce((sum, value) => sum + value, 0) / directOrders.length) : lesson.journeyIndex * 100
  const pathNearTerms = glossary
    .map((term) => ({ term, order: learningMap.get(term.id.toLowerCase())?.learningOrder ?? Number.MAX_SAFE_INTEGER }))
    .filter(({ order, term }) => order !== Number.MAX_SAFE_INTEGER && Math.abs(order - anchor) <= 115 && !directTerms.some((direct) => direct.id === term.id))
    .sort((a, b) => Math.abs(a.order - anchor) - Math.abs(b.order - anchor))
    .map(({ term }) => term)
  const sourceFor = (term) => {
    if (directTerms.some((item) => item.id === term.id)) return 'same-card glossary term'
    if (confusableTerms.some((item) => item.id === term.id)) return 'explicitly confusable glossary metadata'
    if (sameStageTerms.some((item) => item.id === term.id)) return 'same-stage glossary term'
    if (nearbyStageTerms.some((item) => item.id === term.id)) return 'nearby-stage glossary term'
    return 'learning-path-near glossary term'
  }

  const pool = uniqueTerms([
    ...confusableTerms,
    ...directTerms,
    ...sameStageTerms,
    ...pathNearTerms,
    ...nearbyStageTerms
  ]).filter((term) => term.id && !['model-literacy'].includes(term.id))

  return {
    stage,
    directTerms: uniqueTerms(directTerms),
    sameStageTerms: uniqueTerms(sameStageTerms),
    nearbyStageTerms: uniqueTerms(nearbyStageTerms),
    confusableTerms: uniqueTerms(confusableTerms),
    pathNearTerms: uniqueTerms(pathNearTerms),
    distractorPool: pool,
    byKey,
    sourceFor,
    stageNumbers
  }
}

function inferRepresentedTermForChoice(choiceText, context) {
  const text = choiceText.toLowerCase()
  const pairs = [
    [/all ai|every ai|broader ai/, 'ai'],
    [/machine learning|classical ml/, 'machine-learning'],
    [/conscious|reader|mind|thinking|human-like|wisdom/, 'llm'],
    [/hand-written|rulebook|if-then|fixed rule|spreadsheet macro|symbolic/, 'symbolic-ai'],
    [/search engine|searched|web|all files|retrieved|document/, 'rag'],
    [/permanent memory|remember|memory/, 'memory'],
    [/context/, 'context window'],
    [/fine-tun|corporate|house style/, 'fine-tuning'],
    [/train|weight|durably|permanent update/, 'training'],
    [/infer|forward pass|ordinary prompt/, 'inference'],
    [/token id|lookup number|id/, 'token-id'],
    [/token|word|chunk/, 'token'],
    [/embedding|starting vector/, 'embedding'],
    [/hidden state|context-shaped/, 'hidden state'],
    [/attention|aware|awareness/, 'attention'],
    [/mlp|feature/, 'MLP'],
    [/logit|raw score/, 'logits'],
    [/softmax|probabilit/, 'softmax'],
    [/sampling|choose|selected/, 'sampling'],
    [/truth|true|prove|guarantee/, 'probability'],
    [/diffusion|denois|noise/, 'diffusion'],
    [/autoregress|append|repeat/, 'autoregression'],
    [/citation|source/, 'citation'],
    [/ground/, 'grounding']
  ]
  const found = pairs.find(([pattern]) => pattern.test(text))
  if (found) return findTerm(context.byKey, found[1])
  return null
}

function uniqueTerms(terms) {
  const seen = new Set()
  return terms.filter((term) => {
    if (!term || seen.has(term.id)) return false
    seen.add(term.id)
    return true
  })
}

function choiceFromTerm(term, lesson, question, context, index) {
  const text = DISTRACTOR_TEXT_BY_TERM[term.id] || `It is mainly about ${term.term}, not the mechanism named in ${lesson.title}.`
  const source = context.sourceFor(term)
  return {
    choiceId: `choice-${slug(question.questionId)}-${slug(term.id)}-${index}`,
    text,
    isCorrect: false,
    feedback: `This choice points toward ${term.term}. ${term.term} matters nearby, but this question is asking about ${lesson.title}.`,
    representedMisconception: term.confused || `${term.term} is being treated as the same idea as ${lesson.title}.`,
    representedGlossaryTerm: term.id,
    distractorSource: source,
    distractorRationale: `Tempting because ${term.term} is ${source.replace(' glossary term', '')} for this part of the Journey. It is not correct because the question asks for the specific ${lesson.title} mechanism or distinction.`
  }
}

